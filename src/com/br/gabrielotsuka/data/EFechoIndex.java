package com.br.gabrielotsuka.data;

public class EFechoIndex {
    String state;
    Integer position;

    public EFechoIndex(String state, Integer position) {
        this.state = state;
        this.position = position;
    }

    public String getState() {
        return state;
    }

    public Integer getPosition() {
        return position;
    }
}
